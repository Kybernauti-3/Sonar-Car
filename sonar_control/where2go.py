def car_position(map):
    for i in range(len(map)):
        for j in range(len(map[0])):
            if map[i][j] == "S":
                return i, j
    return 5,5

def GenerateMove(map):
    row, col = car_position(map)
    if map[row-1][col] == 0:
        return "Move up"
    elif map[row+1][col] == 0:
        return "Move down"
    elif map[row][col-1] == 0:
        return "Move left"
    elif map[row][col+1] == 0:
        return "Move right"
    else:
        return "No free path available"

